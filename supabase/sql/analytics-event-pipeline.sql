-- Event analytics pipeline tables.
-- NOTE: Existing `analytics` and `listing_analytics` tables are intentionally untouched.

create table if not exists analytics_sessions (
  id uuid primary key default gen_random_uuid(),
  listing_id bigint not null references listings(id) on delete cascade,
  visitor_id text,
  user_id uuid,
  session_source text,
  session_medium text,
  user_agent text,
  ip_hash text,
  started_at timestamptz not null default now(),
  ended_at timestamptz,
  created_at timestamptz not null default now(),
  metadata jsonb not null default '{}'::jsonb
);

create table if not exists analytics_events (
  id bigint generated by default as identity primary key,
  session_id uuid not null references analytics_sessions(id) on delete cascade,
  listing_id bigint not null references listings(id) on delete cascade,
  event_name text not null,
  event_value numeric,
  occurred_at timestamptz not null default now(),
  page_path text,
  referrer text,
  properties jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

-- Optional daily rollups.
create table if not exists analytics_listing_daily (
  day date not null,
  listing_id bigint not null references listings(id) on delete cascade,
  event_name text not null,
  event_count bigint not null default 0,
  unique_sessions bigint not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  primary key (day, listing_id, event_name)
);

create table if not exists analytics_tier_daily (
  day date not null,
  tier text not null,
  event_name text not null,
  event_count bigint not null default 0,
  unique_sessions bigint not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  primary key (day, tier, event_name)
);


-- Indexes for safe query performance.
create index if not exists analytics_events_occurred_at_idx
  on analytics_events (occurred_at desc);

create index if not exists analytics_events_listing_time_idx
  on analytics_events (listing_id, occurred_at desc);

create index if not exists analytics_events_session_time_idx
  on analytics_events (session_id, occurred_at desc);

create index if not exists analytics_events_event_name_time_idx
  on analytics_events (event_name, occurred_at desc);

-- Baseline auth helpers for RLS.
create or replace function analytics_is_admin()
returns boolean
language sql
stable
as $$
  select
    coalesce(auth.jwt() ->> 'role', '') in ('service_role', 'admin')
    or coalesce(auth.jwt() -> 'app_metadata' ->> 'role', '') in ('admin', 'super_admin');
$$;

create or replace function analytics_is_approved_path()
returns boolean
language sql
stable
as $$
  select
    analytics_is_admin()
    or coalesce(auth.jwt() ->> 'path', '') in ('/listings', '/listing', '/listing.html');
$$;

create or replace function analytics_is_listing_owner(target_listing_id bigint)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from business_owners bo
    where bo.listing_id = target_listing_id
      and bo.owner_user_id::text = auth.uid()::text
  );
$$;

alter table analytics_sessions enable row level security;
alter table analytics_events enable row level security;
alter table analytics_listing_daily enable row level security;
alter table analytics_tier_daily enable row level security;

do $$
begin
  if not exists (
    select 1 from pg_policies
    where schemaname = 'public' and tablename = 'analytics_sessions' and policyname = 'Insert analytics sessions from approved path'
  ) then
    create policy "Insert analytics sessions from approved path"
      on analytics_sessions
      for insert
      to anon, authenticated
      with check (analytics_is_approved_path());
  end if;

  if not exists (
    select 1 from pg_policies
    where schemaname = 'public' and tablename = 'analytics_events' and policyname = 'Insert analytics events from approved path'
  ) then
    create policy "Insert analytics events from approved path"
      on analytics_events
      for insert
      to anon, authenticated
      with check (analytics_is_approved_path());
  end if;

  if not exists (
    select 1 from pg_policies
    where schemaname = 'public' and tablename = 'analytics_sessions' and policyname = 'Admins can read analytics sessions'
  ) then
    create policy "Admins can read analytics sessions"
      on analytics_sessions
      for select
      to authenticated
      using (analytics_is_admin());
  end if;

  if not exists (
    select 1 from pg_policies
    where schemaname = 'public' and tablename = 'analytics_events' and policyname = 'Admins can read analytics events'
  ) then
    create policy "Admins can read analytics events"
      on analytics_events
      for select
      to authenticated
      using (analytics_is_admin());
  end if;

  if not exists (
    select 1 from pg_policies
    where schemaname = 'public' and tablename = 'analytics_sessions' and policyname = 'Business owners can read their analytics sessions'
  ) then
    create policy "Business owners can read their analytics sessions"
      on analytics_sessions
      for select
      to authenticated
      using (analytics_is_listing_owner(listing_id));
  end if;

  if not exists (
    select 1 from pg_policies
    where schemaname = 'public' and tablename = 'analytics_events' and policyname = 'Business owners can read their analytics events'
  ) then
    create policy "Business owners can read their analytics events"
      on analytics_events
      for select
      to authenticated
      using (analytics_is_listing_owner(listing_id));
  end if;

  if not exists (
    select 1 from pg_policies
    where schemaname = 'public' and tablename = 'analytics_listing_daily' and policyname = 'Admins can read listing daily rollups'
  ) then
    create policy "Admins can read listing daily rollups"
      on analytics_listing_daily
      for select
      to authenticated
      using (analytics_is_admin());
  end if;

  if not exists (
    select 1 from pg_policies
    where schemaname = 'public' and tablename = 'analytics_listing_daily' and policyname = 'Business owners can read listing daily rollups'
  ) then
    create policy "Business owners can read listing daily rollups"
      on analytics_listing_daily
      for select
      to authenticated
      using (analytics_is_listing_owner(listing_id));
  end if;

  if not exists (
    select 1 from pg_policies
    where schemaname = 'public' and tablename = 'analytics_tier_daily' and policyname = 'Admins can read tier daily rollups'
  ) then
    create policy "Admins can read tier daily rollups"
      on analytics_tier_daily
      for select
      to authenticated
      using (analytics_is_admin());
  end if;
end
$$;

